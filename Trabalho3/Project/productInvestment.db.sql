BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "user" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	"address"	TEXT NOT NULL,
	"zip_code"	TEXT NOT NULL,
	"cpf"	TEXT NOT NULL,
	"pasword"	INTEGER NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "count" (
	"id"	INTEGER NOT NULL,
	"bank"	INTEGER NOT NULL,
	"agency"	INTEGER NOT NULL,
	"number"	TEXT NOT NULL,
	"id_user"	INTEGER NOT NULL,
	FOREIGN KEY("id_user") REFERENCES "user"("id") ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "product" (
	"id"	INTEGER NOT NULL,
	"product_code"	INTEGER NOT NULL,
	"emitter"	TEXT NOT NULL,
	"term"	INTEGER NOT NULL,
	"due_date"	TEXT NOT NULL,
	"timee"	TEXT NOT NULL,
	"minimum_value"	REAL NOT NULL,
	"clazz"	TEXT NOT NULL,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "application" (
	"id"	INTEGER NOT NULL,
	"code"	INTEGER NOT NULL,
	"value"	REAL NOT NULL,
	"application_date"	TEXT NOT NULL,
	"id_count"	INTEGER NOT NULL,
	"id_product"	INTEGER NOT NULL,
	FOREIGN KEY("id_count") REFERENCES "count"("id") ON UPDATE CASCADE,
	FOREIGN KEY("id_product") REFERENCES "product"("id") ON UPDATE CASCADE,
	PRIMARY KEY("id")
);
